import { clsx, type ClassValue } from "clsx"
import { twMerge } from "tailwind-merge"

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs))
}

export function formatSDG(amount: number | string | null | undefined): string {
  const cents = Math.round((Number(amount) || 0) * 100);
  const intPart = Math.floor(cents / 100);
  const decPart = cents % 100;
  // فاصل الآلاف = فاصلة ، فاصل عشري = فاصلة — دائماً رقمان عشريان (1,000,00)
  return intPart.toLocaleString("en-US") + "," + String(decPart).padStart(2, "0");
}
